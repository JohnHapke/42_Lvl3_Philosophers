#include "philo.h"

int	ft_atoi(char *str)
{
	int				sign;
	int				i;
	long long int	result;

	sign = 1;
	i = 0;
	result = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		if (result > MY_INT_MAX || result < MY_INT_MIN)
			return (-1);
		i++;
	}
	if (str[i] != '\0')
		return (-1);
	return (result * sign);
}
